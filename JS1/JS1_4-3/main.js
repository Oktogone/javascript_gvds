"use strict";
// Opérateurs de comparaison...
//   Egalité simple.
console.log(3 == "3"); // true
//   Egalité stricte.
console.log(3 === "3"); // false
//   Non-égalité simple.
console.log(3 != "3"); // false
//   Non-égalité stricte.
console.log(3 !== "3"); // true
//   Inférieur / Supérieur
console.log(3 < 3); // false
console.log(3 >= 3); // true
// Opérateurs logiques.
console.log(2 < 3 && 3 > 4); // false (ET)
console.log(2 < 3 || 3 > 4); // true (OU)
console.log(!(2 < 3)); // false (NON)
