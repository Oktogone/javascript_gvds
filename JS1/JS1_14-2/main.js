"use strict";
// PI
console.log(Math.PI);
// Nombre pseudo-aléatoire.
console.log(Math.random());
// Arrondi supérieur.
console.log(Math.ceil(3.1)); // 4
console.log(Math.ceil(-3.9)); // -3
// Arrondi inférieur.
console.log(Math.floor(3.9)); // 3
console.log(Math.floor(-3.1)); // -4
console.log(parseInt(-3.1)); // -3
// Arrondi au demi supérieur.
console.log(Math.round(3.4)); // 3
console.log(Math.round(3.5)); // 4
// Minimum et maximum d'une série.
console.log(Math.min(12, 8, 13)); // 8
console.log(Math.max(12, 8, 13)); // 13
// Valeur absolue.
console.log(Math.abs(-3)); // 3
console.log(Math.abs(3)); // 3
// Racine carrée.
console.log(Math.sqrt(36)); // 6
